<h1>V1.2 - Quelques fonctionnalités</h1>
<p>Dans cette version, nous nous sommes allé sur les détails, mais aussi sur certaines fonctionnalités. Tout d'abord les petits détails :</p>
<ul>
    <li>La notification lors de l'inscription lorsque qu'un email ou un pseudo est saisi et qu'il est le même qu'un déjà présent dans la base de données</li>
    <li>Un petit message sur la page principale pour montrer sur quel version est le site</li>
    <li>Si un utilisateur choisis un theme, celui-ci est enregistré lors de sa prochaine utilisation</li>
    <li>Si un utilisateur possède un pseudo cours, le texte "Deconnexion" ne dépasse plus</li>
    <li>Le bouton accueil reste afficher en permanence sur l'écran, quelque soit le scroll</li>
</ul>

<h3>XP</h3>
<p>Nous avons mis en place un système d'xp, et pour en gagner il suffit tout simplement de compléter des exercices sur le site. 
    Vous aurez ensuite accès à la correction.<p>
<p>Des récompenses seront mises en places. Devenez de Top1 de DenyCodeBillard. Si vous faites Top3 c'est déjà pas mal</p>

<h3>LeaderBoard</h3>
<p>Nous avons aussi travaillé sur un LeaderBoard suis au système d'xp</p>
<p>Sur le LeaderBoard, vous pourriez voir le Top3 mondial en évidence, et jusqu'au Top100. Essayez d'y être !<p>

<h3>Inscription</h3>
<p>Nous avons aussi apporté une grosse modification lors de l'incription sur le site web. En effet, nous avons apporté une fonctionnalité importante,
    à savoir la vérification d'email. Maintenant, lorsque vous créez un compte, vous recevrez un mail pour valider le votre, et vous disposerez d'un
    lien pour pouvoir le valider.</p>

<h3>Compte utilisateur</h3>
<p>Vous pouvez maintenant vous connectez à l'aide de votre pseudo</p>
<p>Vous avez maintenant la possibilité de changer votre prenom, nom, mot de passe et email. 
    Attention pour ce dernier, cela n'impactera pas pour votre connection, mais s'il nous venait l'idée de vous envoyer un mail, 
    assurez-vous qu'elle soit valide.</p>
<p>Pour votre mot de passe, vous recevrez un mail pour pouvoir ensuite le changer. C'est pour cela qu'il vous faut une adresse mail valide.</p>
<p>Il est aussi possible que vous oubliez votre mot de passe. Dans ce cas, vous pouvez cliquer sur "Mot de passe oublié" et vous recevrez un email
    pour pouvoir le changer après avoir renseigné soit votre email soit votre pseudo. Si en plus de ça vous oubliez votre email ou votre pseudo,
    et bien... bien joué. Nous ne pouvons plus rien faire pour vous !</p>

<h3>Theme</h3>
<p>Le theme sombre est maintenant effectif sur l'ensemble du site sauf les pages d'exercices, qui reste un domaine en très grand travaux,
    et sur les pages de changement et de récupération de mot de passe, qui restent des pages où nous voulions tout d'abord privilégier la simplicité
    d'utilisation.</p>

<h3>Scroll</h3>
<p>Le scroll est désormais sans latence, à la demande d'une grande partie de la communauté.</p>