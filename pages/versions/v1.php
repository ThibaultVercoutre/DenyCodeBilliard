<h3>Les classements</h3>
<p>Vous avez aussi accès aux classements, qui vous permettent de voir les classements par language, par notion, ou par exercice
    par rapport au nombre de visite sur chaque exercices. Il vous sera possible de voir quels exercices seraient trop surcôtés,
    et pouvoir devenir un développeur original, qui n'a pas peur de se démarquer des autres.</p>

<h3>L'exercice de la semaine</h3>
<p>Chaque semaine, un exercice sera proposer, il vous permettra de parcourir plusieurs notions représentées par plusieurs parties à l'exercice. 
    Une correction sera apporté à chaque fin de semaine. Elle n'en restera pas moins unique. Le principal étant que vous ayez compris l'exercice.
    Après cela, l'exercice de la semaine sera découpé par parties, et donc par notion, et mis à disposition en tant que plusieurs petits exercices
    lié entre eux, avec leur correction</p>

