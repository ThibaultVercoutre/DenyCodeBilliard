<h1>V1.3 - Résolution de problèmes et amélioration de compte (6 mai 2023)</h1>

<h3>Problèmes</h3>
<p>Comme vous l'aurez de deviné, il y a eu certain problème. Et si vous aviez essayé de vous incrire, changé votre mot de passe, ou le réinitialisé
    parce que vous l'aviez oublié, vous avez sans doute remarqué que vous n'aviez reçu aucun mail. C'est normal, et nous l'avons vu dès le lancement
    de la version 1.2. Nous avons donc travaillé sur ce problème, et nous avons trouvé la solution. Vous pouvez donc maintenant vous inscrire, changer
    votre mot de passe, ou le réinitialiser, et vous recevrez un mail.</p>
<p>Il y a aussi eu quelques correction d'interface utilisateur, comme :</p>
<ul>
    <li>Le bouton "thème" qui se mettait en gras alors qu'il ne l'était pas de base</li>
</ul>

<h3>Amélioration de compte</h3>
<p>Nous avons ajouté une multitude de petites features. Comme les trophées, qui sont des succès réalisable sur le site. Découvrez les, et vous
    aurez de belles récompenses.</p>
<p>Aussi, nous avons mis en place un système d'étoiles, qui seront des points utilisables dans un exercice pour avoir un indice. Vous pouvez
    gagner des étoiles en complétant des exercices.</p>